package ex_02_Operators;

public class Lab009_LogicalOperator {
    public static void main(String[] args) {
        boolean a= true;
        boolean b= false;
        boolean result;
         result=a||b;// Logical OR
        System.out.println(result);
        result=a&&b;
        System.out.println(result);//Logical AND
        result=!b; //Logical not
        System.out.println(result);


    }
}
