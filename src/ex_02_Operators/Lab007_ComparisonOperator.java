package ex_02_Operators;

public class Lab007_ComparisonOperator {
    public static void main(String[] args) {
        int a=20;
        int b=14;
        int c=20;

        System.out.println( a==b);//false--> equal to operator
        System.out.println(a==c);//true
        System.out.println(a>b);// true --> greater than operator
        System.out.println(a<b);//false --> less than operator
        System.out.println(a>=c);
        System.out.println(a<=b);
    }
}
