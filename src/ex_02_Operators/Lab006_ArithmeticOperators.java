package ex_02_Operators;

public class Lab006_ArithmeticOperators {
    public static void main(String[] args) {
        int a=10;
        int b=3;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);//op is quotient
        System.out.println(a%b);// op is remainder
    }
}
