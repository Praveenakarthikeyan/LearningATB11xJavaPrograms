package ex_08_forLoop;

public class Lab041_For_P1 {
    public static void main(String[] args) {
        // For Loop
        // Help you to repeat a block of code.

        // Initialization -> Condition -> Updation(Incre/decre)
        // ICU

        for(int i=0;i<10;i++){
            System.out.println(i);
        }
    }



}
